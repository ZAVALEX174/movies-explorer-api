const mongoose = require('mongoose');
const { validateLink } = require('../utils/validate');

const movieSchema = new mongoose.Schema(
  {
    country: {
      type: String,
      required: true,
    },
    director: {
      type: String,
      required: true,
    },
    duration: {
      type: Number,
      required: true,
    },
    year: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
      validate: validateLink,
    },
    trailerLink: {
      type: String,
      required: true,
      validate: validateLink,
    },
    thumbnail: {
      type: String,
      required: true,
      validate: validateLink,
    },
    owner: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'user',
      required: true,
    },
    movieId: {
      type: Number,
      required: true,
    },
    nameRU: {
      type: String,
      required: true,
    },
    nameEN: {
      type: String,
      required: true,
    },
  },
  { versionKey: false },
);

const Movie = mongoose.model('movie', movieSchema);

module.exports = { Movie };
